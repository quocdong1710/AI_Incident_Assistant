CREATE TABLE incident (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    incident_id VARCHAR(255) NOT NULL UNIQUE,
    title VARCHAR(120) NOT NULL,
    description VARCHAR(4000),
    severity VARCHAR(32),
    status VARCHAR(32),
    component VARCHAR(255),
    environment VARCHAR(255),
    source_language VARCHAR(255),
    impact_scope VARCHAR(255),
    jira_issue_key VARCHAR(255),
    jira_issue_url VARCHAR(255),
    groups_reported INTEGER DEFAULT 1,
    platforms VARCHAR(255),
    first_reported_at TIMESTAMP WITH TIME ZONE,
    sla_deadline_at TIMESTAMP WITH TIME ZONE,
    sla_warning_sent_at TIMESTAMP WITH TIME ZONE,
    first_response_at TIMESTAMP WITH TIME ZONE,
    sla_breached BOOLEAN NOT NULL DEFAULT FALSE,
    escalation_level INTEGER DEFAULT 0,
    assigned_to VARCHAR(255),
    confidence_score DOUBLE PRECISION NOT NULL DEFAULT 0,
    created_at TIMESTAMP WITH TIME ZONE,
    updated_at TIMESTAMP WITH TIME ZONE
);

CREATE INDEX idx_incident_jira_issue_key ON incident (jira_issue_key);
CREATE INDEX idx_incident_status ON incident (status);
CREATE INDEX idx_incident_severity ON incident (severity);
CREATE INDEX idx_incident_sla_deadline_at ON incident (sla_deadline_at);

CREATE TABLE incident_source (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    incident_pk BIGINT,
    incident_id VARCHAR(255),
    jira_issue_key VARCHAR(255),
    message_id VARCHAR(255),
    chat_id VARCHAR(255),
    platform VARCHAR(32),
    sender_id VARCHAR(255),
    sender_name VARCHAR(255),
    received_at TIMESTAMP WITH TIME ZONE
);

CREATE INDEX idx_incident_source_incident_id ON incident_source (incident_id);
CREATE INDEX idx_incident_source_message ON incident_source (message_id, chat_id, platform);

CREATE TABLE message_log (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    message_id VARCHAR(255) NOT NULL,
    platform VARCHAR(32) NOT NULL,
    group_id VARCHAR(255) NOT NULL,
    group_name VARCHAR(255),
    sender_id VARCHAR(255) NOT NULL,
    sender_name VARCHAR(255),
    message_text VARCHAR(4000),
    mentioned_user_ids VARCHAR(255),
    received_at TIMESTAMP WITH TIME ZONE,
    classification VARCHAR(255),
    confidence_score DOUBLE PRECISION NOT NULL DEFAULT 0
);

CREATE UNIQUE INDEX uk_message_log_idempotency ON message_log (message_id, group_id, platform);
CREATE INDEX idx_message_log_received_at ON message_log (received_at);
CREATE INDEX idx_message_log_classification ON message_log (classification);

CREATE TABLE engineer (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(255),
    display_name VARCHAR(255),
    skills VARCHAR(255),
    active_incident_count INTEGER NOT NULL DEFAULT 0,
    online BOOLEAN NOT NULL DEFAULT TRUE,
    access_components VARCHAR(255),
    recent_assignments INTEGER NOT NULL DEFAULT 0
);

CREATE UNIQUE INDEX uk_engineer_username ON engineer (username);
CREATE INDEX idx_engineer_online ON engineer (online);

CREATE TABLE sla_breach_log (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    incident_id VARCHAR(255),
    jira_issue_key VARCHAR(255),
    severity VARCHAR(32),
    sla_duration_minutes INTEGER NOT NULL DEFAULT 0,
    breach_at TIMESTAMP WITH TIME ZONE,
    first_response_at TIMESTAMP WITH TIME ZONE,
    delay_minutes BIGINT NOT NULL DEFAULT 0,
    escalation_level INTEGER NOT NULL DEFAULT 0
);

CREATE INDEX idx_sla_breach_log_breach_at ON sla_breach_log (breach_at);
CREATE INDEX idx_sla_breach_log_jira_issue_key ON sla_breach_log (jira_issue_key);
